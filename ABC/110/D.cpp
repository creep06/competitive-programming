int n, m, res = 1, pa[120120][120];

// メモ化再帰(パスカルの三角形)でnCrを求める O(NR)
int comb(int n, int r) {
	if (r<0 || r>n) return 0;
	else if (r==0) return 1;
	else if (pa[n][r]) return pa[n][r];
	else return pa[n][r] = (comb(n-1, r-1) + comb(n-1, r)) %mod;
}

// 素因数分解 O(√N)
map<ll, int> pf(ll n) {
	map<ll, int> res;
	for (ll i = 2; i*i <= n; i++)
		while (n%i==0) ++res[i], n /= i;
	if (n!=1) res[n]++;
	return res;
}

signed main() {
	cin >> n >> m;
	map<ll,int> p = pf(m);
	each(i,p) (res *= comb(n+i.second-1,i.second)) %= mod;
	cout << res << endl;
}
