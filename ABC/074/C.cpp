int m1, m2, s1, s2, thaw, mx, ans1, ans2;

int main() {
	cin >> m1 >> m2 >> s1 >> s2 >> thaw >> mx;
	m1 *= 100, m2 *= 100;
	
	for (int i = 0; i*m1 <= mx; ++i) {
		for (int j = 0; i*m1 + j*m2 <= mx; ++j) {
			for (int k = 0; i*m1 + j*m2 + s1*k <= mx; ++k) {
				for (int l = 0; i*m1 + j*m2 + s1*k + s2*l <= mx; ++l) {
					int m = m1*i + m2*j;
					int s = s1*k + s2*l;
					// 水mに対して砂糖sが溶けきらない場合
					if (s > m/100*thaw) break;
					if (ans2*(m+s) <= ans1*s) {
						ans1 = m + s;
						ans2 = s;
					}
				}
			}
		}
	}
	cout << ans1 << " " << ans2 << ln;
}