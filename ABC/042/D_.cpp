ll mpow(ll x, ll n, ll m) {
	ll res = 1;
	while (n>0) {
		if (n&1) res = res * x %m;
		x = x * x %m;
		n >>= 1;
	}
	return res;
}

ll comb(ll n, ll r) {
	return f[n]*fr[r] %mod *fr[n-r] %mod;
}

