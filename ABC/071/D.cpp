// fは直前のドミノが縦2が1個パターンなら1、横2が2個パターンなら2
int n, f;
string s1, s2;
ll ans;

int main() {
	cin >> n >> s1 >> s2;
	
	// これは公式の解説の図が一番わかりやすい
	// https://atcoder.jp/img/arc081/editorial.pdf
	rep(i,n) {
		// 縦2のドミノ1個の場合
		if (s1[i]==s2[i]) {
			if (f==1) ans = ans * 2 % mod;
			else if (f==0) ans = 3;
			f = 1;
		}
		// そうでない場合 横2のドミノが縦に2個繋がってる
		else {
			if (f==1) ans = ans * 2 % mod, f = 2;
			else if (f==2) ans = ans * 3 % mod;
			else ans = 6;
			f = 2;
			i++;
		}
	}
	cout << ans << ln;
}