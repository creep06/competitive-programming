ll n, x=1;

int main() {
	cin >> n;
	int depth = 0;
	for (ll i = n; i > 1; i/=2) depth++;
	int turn = 0;
	while (true) {
		if (turn==0) {
			if (depth%2==0) x = x*2 + 1;
			else x = x*2;
		} else {
			if (depth%2==0) x = x*2;
			else x = x*2 + 1;
		}
		if (x>n) break;
		turn = 1 - turn;
	}
	if (turn==0) cout << "Aoki" << ln;
	else cout << "Takahashi" << ln;
}