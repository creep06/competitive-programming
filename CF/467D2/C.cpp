// ジュリアはd秒ごとにストーブを付けなおしに行き、最後に付いてからk秒後にストーブは止まる
// チキンが焼きあがるまでストーブオンの状態でt秒かかる ストーブオフなら倍かかる 何秒で1個焼けるか？
// 周期を使う

ll k, d, t, c, con, coff, one, ans;

signed main() {
	cin >> k >> d >> t;
	if (k%d==0) return cout << t << ln, 0;
	k *= 2, d *= 2, t *= 2;
	if (k<d) c = d, con = k, coff = c - con;
	else c = (k/d+1)*d, con = k, coff = c - con;
	one = con + coff/2;
	ans = t/one * c;
	t %= one;
	if (t<=con) ans += t;
	else {
		ans += con;
		t -= con;
		ans += t*2;
	}
	printf("%.1lf\n",1.*ans/2);
}