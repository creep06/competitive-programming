bool isprime(int n) {
	if (n==1) return false;
	for (int i = 2; i*i <= n; ++i) {
		if (n%i==0) return false;
	}
	return true;
}

ll primefactor(ll n) {
	ll res = linf;
	int tim = 0;
	for (ll i = 2; i*i <= n; i++) {
		while (n%i==0) {
			chmin(res, i);
			n /= i;
			cout << tim++ << ' ';
		}
	}
	if (n!=1) chmin(res, n);
	return res;
}

