/*
 * (問題)
 * ネズミがt=0に、ネコがt=Tに同じ入口からチューブに入る
 * ネズミは出来る限り長くネコから逃げたくて、ネコは最速でネズミを捕まえたい
 * お互い最善を尽くしたときネコがネズミを捕まえるまでに何秒かかるか
 *
 * (解法)
 * (ネコの最高速度)<=(ネズミの最高速度)のときはネズミがネコと同じ速度で逃げ続ければ絶対に捕まえられない
 * そうじゃないときはネコがチューブに入るまでにネズミはなるべく入り口から遠い点に行けばよく、二者の相対速度で距離が縮んでいく
 */

class CatAndRat {
	public:
	double getTime(int r, int t, int vr, int vc) {
		return vr>=vc ? -1 : min(M_PI*r, 1.*vr*t) / (vc-vr);
	}
};