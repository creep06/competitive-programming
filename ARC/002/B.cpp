bool uru(int n) {
	return n % 400 == 0 || (n % 100 != 0 && n % 4 == 0);
}

void fw() {
	d++;
	// 月の変化
	if (m==2 && d==29 && !uru(y)) m++, d = 1;
	else if (m==2 && d==30) m++, d = 1;
	else if (th[m] && d==31) m++, d = 1;
	else if (d==32) m++, d = 1;
	// 年の変化
	if (m==13) y++, m = 1;
}

