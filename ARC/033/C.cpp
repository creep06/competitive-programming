int sum(int i) {
	int s = 0;
	while (i>0) {
		s += bit[i];
		i -= i & -i;
	}
	return s;
}

void add(int i, int x) {
	while (i<=200000) {
		bit[i] += x;
		i += i & -i;
	}
}

// k番目に小さい数字を二分探索
int bs(int k) {
	int l = 0, r = 200000;
	while (r-l>1) {
		int m = (r+l)/2;
		if (sum(m)<k) l = m;
		else r = m;
	}
	return r;
}

