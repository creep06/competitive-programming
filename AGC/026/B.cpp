// ↓の最初のaから1ループごとにd%bだけ増えてって、
// cを超えたところでbぶん減った値に行ってまた同様のループが始まる
// この最中に区間(c,b)に入ってしまうと買えなくなって終わる
// c+1 <= (a+pk)%b (< b) が成り立つkが存在するとNo
// これもまたb<=pkを満たすkの最小値mについてpm%bだけずれるループになる
// この値がb-c以下ならNo

int q, a, b, c, d;

signed main() {
	cin >> q;
	while (q--) {
		cin >> a >> b >> c >> d;
		if (b>d || a<b) {
			cout << "No" << ln;
			continue;
		}
		int k = max((a-c+b-1)/b, 1ll);
		a -= b*k;
		if (a<0) {
			cout << "No" << ln;
			continue;
		}
		
		int p = d %b;
		if (p==0) {
			cout << "Yes" << ln;
			continue;
		}
		k = (b+p-1)/p;
		k = p*k %b;
		// pとkを数個ずつ足して作れる数のうちcを超える最小値がb未満ならNo
		int m = LONG_LONG_MAX;
		if (k!=0) chmin(m,k);
		if (p!=0) chmin(m,p);
		if (k!=0 && p!=0) chmin(m, __gcd(k,p));
		k = c/m + 1;
		k *= m;
		if (c<k && k<b) cout << "No" << ln;
		else cout << "Yes" << ln;
	}
}