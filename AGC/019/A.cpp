ll a1, a2, a4, a8, n, r;

int main() {
	cin >> a1 >> a2 >> a4 >> a8 >> n;
	a1 *= 4, a2 *= 4, a4 *= 4, a8 *= 4, n*= 4;
	if (n>=8) {
		ll mn = min(8*a1, min(4*a2, min(2*a4, a8)));
		ll m = n/8;
		r += mn * m;
		n -= m*8;
	}
	if (n>=4) {
		ll mn = min(4*a1, min(2*a2, a4));
		ll m = n/4;
		r += mn * m;
		n -= m*4;
	}
	if (n>=2) {
		ll mn = min(2*a1, a2);
		ll m = n/2;
		r += mn * m;
		n -= m*2;
	}
	if (n==1) {
		r += a1;
		n -= 1;
	}
	cout << r/4 << ln;
}