struct UF {
	vector<int> p;
	UF(int n) : p(n, -1) {};
	bool unite(int u, int v) {
		if ((u = find(u)) == (v = find(v))) return false;
		if (p[u] > p[v]) swap(u, v);
		p[u] += p[v]; p[v] = u;
		return true;
	}
	bool same(int u, int v) { return find(u) == find(v); }
	int find(int u) { return p[u] < 0 ? u : p[u] = find(p[u]); }
	int usize(int u) { return -p[find(u)]; }
};

int kruskal(pair<int,pair<int,int> > ng) {
	sort(e.begin(),e.end());
	UF uf(n);
	ee.clear();
	int res = 0;
	for (int i=0; i<m; ++i) {
		if (e[i]==ng) continue;
		if (!uf.same(e[i].se.fi, e[i].se.se)) {
			uf.unite(e[i].se.fi, e[i].se.se);
			res += e[i].fi;
			ee.pb(e[i]);
		}
	}
	if (uf.usize(0)<n) return inf;
	return res;
}

